package hari1;

public class Hari18 {
    public static void main(String[] args) {
        //BELAJAR TIPE DATA
        // Deklarasi variabel dengan tipe data String
        String nama = "Arfina";
        String status = "Mahasiswa";
        
        // Deklarasi variabel dengan tipe data integer
        int umur = 20;
        
        // Deklarasi variabel dengan tipe data double
        double tinggi = 160.0;
        double berat_badan = 50.0;

        // Menampilkan biodata ke layar
        System.out.println("Biodata Saya");
        System.out.println("Nama: " + nama);
        System.out.println("Status: " + status);
        System.out.println("Umur: " + umur + " tahun");
        System.out.println("Tinggi: " + tinggi + " cm");
        System.out.println("Berat badan: " + berat_badan + " kg");
    }
    
}

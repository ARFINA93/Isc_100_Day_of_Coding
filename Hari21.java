package hari1;

public class Hari21 {
    public static void main(String[] args) {
        //belajar perulangan while
        int a = 1;
        while(a < 31){ //ini adalah pernyataan yang akan dieksekusi selama nilai a kurang dari 31
            System.out.println("Angka ke-" + a); //perintah yang akan diulang
            a++; // increment variabel
        }
        
    }
    
}
